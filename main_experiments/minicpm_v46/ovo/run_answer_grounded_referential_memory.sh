#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
PYTHON_BIN="${PYTHON_BIN:-$(command -v python || command -v python3)}"
NUM_PROCESSES="${NUM_PROCESSES:-8}"
MAIN_PROCESS_PORT="${MAIN_PROCESS_PORT:-29882}"
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION:-sdpa}"
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE:-16x}"
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS:-1}"
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS:-1}"
MINICPM_QA_DEVICE="${MINICPM_QA_DEVICE:-}"
MINICPM_LOAD_ON_CPU_FIRST="${MINICPM_LOAD_ON_CPU_FIRST:-0}"
HF_ENABLE_PARALLEL_LOADING="${HF_ENABLE_PARALLEL_LOADING:-false}"
HF_PARALLEL_LOADING_WORKERS="${HF_PARALLEL_LOADING_WORKERS:-1}"
HF_DEACTIVATE_ASYNC_LOAD="${HF_DEACTIVATE_ASYNC_LOAD:-1}"
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD:-1}"
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT:-7200}"
REFERENTIAL_RECENT_WINDOW="${REFERENTIAL_RECENT_WINDOW:-6}"
REFERENTIAL_REFERENCE_FRAMES="${REFERENTIAL_REFERENCE_FRAMES:-2}"
REFERENTIAL_MEMORY_ANCHOR_FRAMES="${REFERENTIAL_MEMORY_ANCHOR_FRAMES:-1}"
MINICPM_REF_CLIP_MODEL="${MINICPM_REF_CLIP_MODEL:-openai/clip-vit-base-patch32}"
MINICPM_REF_CLIP_DEVICE="${MINICPM_REF_CLIP_DEVICE:-}"
MAX_SAMPLES_PER_SPLIT="${MAX_SAMPLES_PER_SPLIT:-}"

OVO_ANNO_PATH="${REPO_ROOT}/data/ovo_bench/ovo_bench_new.json"
OVO_CHUNKED_DIR="${REPO_ROOT}/data/ovo_bench/chunked_videos"
OVO_RESULT_DIR="${OVO_RESULT_DIR:-${REPO_ROOT}/main_experiments/results/repro_referential/ovo_minicpmv46_answer_grounded_referential_memory_recent${REFERENTIAL_RECENT_WINDOW}_r${REFERENTIAL_REFERENCE_FRAMES}_a${REFERENTIAL_MEMORY_ANCHOR_FRAMES}_d8}"

cd "${REPO_ROOT}"
if [[ -z "${CUDA_VISIBLE_DEVICES:-}" ]]; then
    CUDA_VISIBLE_DEVICES="0,1,2,3,4,5,6,7"
fi

echo "[INFO] Using PYTHON_BIN=${PYTHON_BIN}"
echo "[INFO] Using CUDA_VISIBLE_DEVICES=${CUDA_VISIBLE_DEVICES}"
echo "[INFO] Using NUM_PROCESSES=${NUM_PROCESSES}"
echo "[INFO] Using REFERENTIAL_RECENT_WINDOW=${REFERENTIAL_RECENT_WINDOW}"
echo "[INFO] Using REFERENTIAL_REFERENCE_FRAMES=${REFERENTIAL_REFERENCE_FRAMES}"
echo "[INFO] Using REFERENTIAL_MEMORY_ANCHOR_FRAMES=${REFERENTIAL_MEMORY_ANCHOR_FRAMES}"
echo "[INFO] Using MINICPM_REF_CLIP_MODEL=${MINICPM_REF_CLIP_MODEL}"
echo "[INFO] Using MINICPM_REF_CLIP_DEVICE=${MINICPM_REF_CLIP_DEVICE}"
echo "[INFO] Results: ${OVO_RESULT_DIR}"

COMMON_ARGS=(
    --model_path "openbmb/MiniCPM-V-4.6" \
    --anno_path "${OVO_ANNO_PATH}" \
    --chunked_dir "${OVO_CHUNKED_DIR}" \
    --result_dir "${OVO_RESULT_DIR}" \
    --recent_frames_only "${REFERENTIAL_RECENT_WINDOW}" \
    --reference_frames "${REFERENTIAL_REFERENCE_FRAMES}" \
    --memory_anchor_frames "${REFERENTIAL_MEMORY_ANCHOR_FRAMES}" \
    --memory_clip_model "${MINICPM_REF_CLIP_MODEL}" \
    --chunk_duration 1.0 \
    --fps 1.0 \
    --max_qa_tokens 256
)
if [[ -n "${MINICPM_QA_DEVICE}" ]]; then
    COMMON_ARGS+=(--qa_device "${MINICPM_QA_DEVICE}")
fi
if [[ -n "${MINICPM_REF_CLIP_DEVICE}" ]]; then
    COMMON_ARGS+=(--memory_clip_device "${MINICPM_REF_CLIP_DEVICE}")
fi
if [[ -n "${MAX_SAMPLES_PER_SPLIT}" ]]; then
    COMMON_ARGS+=(--max_samples_per_split "${MAX_SAMPLES_PER_SPLIT}")
fi

CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES}" \
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION}" \
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE}" \
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS}" \
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS}" \
MINICPM_LOAD_ON_CPU_FIRST="${MINICPM_LOAD_ON_CPU_FIRST}" \
HF_ENABLE_PARALLEL_LOADING="${HF_ENABLE_PARALLEL_LOADING}" \
HF_PARALLEL_LOADING_WORKERS="${HF_PARALLEL_LOADING_WORKERS}" \
HF_DEACTIVATE_ASYNC_LOAD="${HF_DEACTIVATE_ASYNC_LOAD}" \
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD}" \
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT}" \
MINICPM_REF_CLIP_MODEL="${MINICPM_REF_CLIP_MODEL}" \
MINICPM_REF_CLIP_DEVICE="${MINICPM_REF_CLIP_DEVICE}" \
"${PYTHON_BIN}" -m accelerate.commands.launch \
    --num_processes "${NUM_PROCESSES}" \
    --main_process_port "${MAIN_PROCESS_PORT}" \
    --multi_gpu \
    --mixed_precision bf16 \
    main_experiments/minicpm_v46/ovo/eval_answer_grounded_referential_memory.py "${COMMON_ARGS[@]}"
