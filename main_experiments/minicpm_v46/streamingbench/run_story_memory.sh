#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
PYTHON_BIN="${PYTHON_BIN:-$(command -v python || command -v python3)}"
NUM_PROCESSES="${NUM_PROCESSES:-8}"
MAIN_PROCESS_PORT="${MAIN_PROCESS_PORT:-29861}"
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION:-sdpa}"
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE:-16x}"
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS:-1}"
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS:-1}"
MINICPM_QA_DEVICE="${MINICPM_QA_DEVICE:-}"
MINICPM_LOAD_ON_CPU_FIRST="${MINICPM_LOAD_ON_CPU_FIRST:-0}"
HF_ENABLE_PARALLEL_LOADING="${HF_ENABLE_PARALLEL_LOADING:-false}"
HF_PARALLEL_LOADING_WORKERS="${HF_PARALLEL_LOADING_WORKERS:-1}"
HF_DEACTIVATE_ASYNC_LOAD="${HF_DEACTIVATE_ASYNC_LOAD:-1}"
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD:-1}"
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT:-7200}"
STORY_RECENT_FRAMES="${STORY_RECENT_FRAMES:-6}"
STORY_BATCH_SIZE="${STORY_BATCH_SIZE:-8}"
STORY_MAX_ITEMS="${STORY_MAX_ITEMS:-96}"
STORY_MAX_PROMPT_CHARS="${STORY_MAX_PROMPT_CHARS:-9000}"
STORY_DESC_MAX_TOKENS="${STORY_DESC_MAX_TOKENS:-192}"
STORY_DESCRIBE_STRIDE="${STORY_DESCRIBE_STRIDE:-1}"
STORY_PROMPT_TAG="${STORY_PROMPT_TAG:-v2}"
STORY_CONTEXT_TIME="${STORY_CONTEXT_TIME:--1}"
MAX_SAMPLES="${MAX_SAMPLES:-}"

SB_ANNO_PATH="${REPO_ROOT}/data/streamingbench/questions_real.json"
SB_VIDEO_DIR="${REPO_ROOT}/data/streamingbench/videos"
SB_RESULT_DIR="${SB_RESULT_DIR:-${REPO_ROOT}/main_experiments/results/repro_story_memory/streamingbench_minicpmv46_story_memory_recent${STORY_RECENT_FRAMES}_b${STORY_BATCH_SIZE}_m${STORY_MAX_ITEMS}_s${STORY_DESCRIBE_STRIDE}_${STORY_PROMPT_TAG}_d8}"

cd "${REPO_ROOT}"
if [[ -z "${CUDA_VISIBLE_DEVICES:-}" ]]; then
    CUDA_VISIBLE_DEVICES="0,1,2,3,4,5,6,7"
fi

echo "[INFO] Using PYTHON_BIN=${PYTHON_BIN}"
echo "[INFO] Using CUDA_VISIBLE_DEVICES=${CUDA_VISIBLE_DEVICES}"
echo "[INFO] Using NUM_PROCESSES=${NUM_PROCESSES}"
echo "[INFO] Using STORY_RECENT_FRAMES=${STORY_RECENT_FRAMES}"
echo "[INFO] Using STORY_BATCH_SIZE=${STORY_BATCH_SIZE}"
echo "[INFO] Using STORY_MAX_ITEMS=${STORY_MAX_ITEMS}"
echo "[INFO] Using STORY_MAX_PROMPT_CHARS=${STORY_MAX_PROMPT_CHARS}"
echo "[INFO] Using STORY_DESC_MAX_TOKENS=${STORY_DESC_MAX_TOKENS}"
echo "[INFO] Using STORY_DESCRIBE_STRIDE=${STORY_DESCRIBE_STRIDE}"
echo "[INFO] Using STORY_PROMPT_TAG=${STORY_PROMPT_TAG}"
echo "[INFO] Using STORY_CONTEXT_TIME=${STORY_CONTEXT_TIME}"
echo "[INFO] Results: ${SB_RESULT_DIR}"

COMMON_ARGS=(
    --anno-path "${SB_ANNO_PATH}" \
    --video-dir "${SB_VIDEO_DIR}" \
    --qa-model "openbmb/MiniCPM-V-4.6" \
    --top-k 0 \
    --recent-frames-only "${STORY_RECENT_FRAMES}" \
    --chunk-duration 1.0 \
    --fps 1.0 \
    --context-time "${STORY_CONTEXT_TIME}" \
    --max-qa-tokens 256 \
    --output-dir "${SB_RESULT_DIR}" \
    --story-recent-frames "${STORY_RECENT_FRAMES}" \
    --story-batch-size "${STORY_BATCH_SIZE}" \
    --story-max-items "${STORY_MAX_ITEMS}" \
    --story-max-prompt-chars "${STORY_MAX_PROMPT_CHARS}" \
    --story-desc-max-tokens "${STORY_DESC_MAX_TOKENS}" \
    --story-describe-stride "${STORY_DESCRIBE_STRIDE}"
)
if [[ -n "${MINICPM_QA_DEVICE}" ]]; then
    COMMON_ARGS+=(--qa-device "${MINICPM_QA_DEVICE}")
fi
if [[ -n "${MAX_SAMPLES}" ]]; then
    COMMON_ARGS+=(--max-samples "${MAX_SAMPLES}")
fi

CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES}" \
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION}" \
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE}" \
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS}" \
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS}" \
MINICPM_LOAD_ON_CPU_FIRST="${MINICPM_LOAD_ON_CPU_FIRST}" \
HF_ENABLE_PARALLEL_LOADING="${HF_ENABLE_PARALLEL_LOADING}" \
HF_PARALLEL_LOADING_WORKERS="${HF_PARALLEL_LOADING_WORKERS}" \
HF_DEACTIVATE_ASYNC_LOAD="${HF_DEACTIVATE_ASYNC_LOAD}" \
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD}" \
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT}" \
MINICPM_STORY_RECENT_FRAMES="${STORY_RECENT_FRAMES}" \
MINICPM_STORY_BATCH_SIZE="${STORY_BATCH_SIZE}" \
MINICPM_STORY_MAX_ITEMS="${STORY_MAX_ITEMS}" \
MINICPM_STORY_MAX_PROMPT_CHARS="${STORY_MAX_PROMPT_CHARS}" \
MINICPM_STORY_DESC_MAX_TOKENS="${STORY_DESC_MAX_TOKENS}" \
MINICPM_STORY_DESCRIBE_STRIDE="${STORY_DESCRIBE_STRIDE}" \
MINICPM_STORY_PROMPT_VERSION="${STORY_PROMPT_TAG}" \
"${PYTHON_BIN}" -m accelerate.commands.launch \
    --num_processes "${NUM_PROCESSES}" \
    --main_process_port "${MAIN_PROCESS_PORT}" \
    --multi_gpu \
    --mixed_precision bf16 \
    main_experiments/minicpm_v46/streamingbench/eval_story_memory_dist.py "${COMMON_ARGS[@]}"
