#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
PYTHON_BIN="${PYTHON_BIN:-$(command -v python || command -v python3)}"
NUM_PROCESSES="${NUM_PROCESSES:-8}"
MAIN_PROCESS_PORT="${MAIN_PROCESS_PORT:-29831}"
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION:-sdpa}"
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE:-16x}"
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS:-1}"
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS:-1}"
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD:-1}"
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT:-7200}"
ADAPTIVE_MODE="${ADAPTIVE_MODE:-adaptive}"
ADAPTIVE_MIN_WINDOW="${ADAPTIVE_MIN_WINDOW:-4}"
ADAPTIVE_MID_WINDOW="${ADAPTIVE_MID_WINDOW:-6}"
ADAPTIVE_MAX_WINDOW="${ADAPTIVE_MAX_WINDOW:-8}"
ADAPTIVE_DEDUP_THRESHOLD="${ADAPTIVE_DEDUP_THRESHOLD:-4.0}"
ADAPTIVE_DEDUP_MIN_FRAMES="${ADAPTIVE_DEDUP_MIN_FRAMES:-4}"
ADAPTIVE_MEMORY_ANCHORS="${ADAPTIVE_MEMORY_ANCHORS:-2}"
ADAPTIVE_MEMORY_SEARCH_CHUNKS="${ADAPTIVE_MEMORY_SEARCH_CHUNKS:-0}"
MAX_SAMPLES_PER_SPLIT="${MAX_SAMPLES_PER_SPLIT:-}"

OVO_ANNO_PATH="${REPO_ROOT}/data/ovo_bench/ovo_bench_new.json"
OVO_CHUNKED_DIR="${REPO_ROOT}/data/ovo_bench/chunked_videos"
OVO_RESULT_DIR="${OVO_RESULT_DIR:-${REPO_ROOT}/main_experiments/results/repro_adaptive/ovo_minicpmv46_${ADAPTIVE_MODE}_w${ADAPTIVE_MIN_WINDOW}-${ADAPTIVE_MID_WINDOW}-${ADAPTIVE_MAX_WINDOW}_d8}"

cd "${REPO_ROOT}"
if [[ -z "${CUDA_VISIBLE_DEVICES:-}" ]]; then
    CUDA_VISIBLE_DEVICES="0,1,2,3,4,5,6,7"
fi

echo "[INFO] Using PYTHON_BIN=${PYTHON_BIN}"
echo "[INFO] Using CUDA_VISIBLE_DEVICES=${CUDA_VISIBLE_DEVICES}"
echo "[INFO] Using NUM_PROCESSES=${NUM_PROCESSES}"
echo "[INFO] Using ADAPTIVE_MODE=${ADAPTIVE_MODE}"
echo "[INFO] Using adaptive windows ${ADAPTIVE_MIN_WINDOW}/${ADAPTIVE_MID_WINDOW}/${ADAPTIVE_MAX_WINDOW}"
echo "[INFO] Using ADAPTIVE_DEDUP_THRESHOLD=${ADAPTIVE_DEDUP_THRESHOLD}"
echo "[INFO] Using ADAPTIVE_DEDUP_MIN_FRAMES=${ADAPTIVE_DEDUP_MIN_FRAMES}"
echo "[INFO] Using ADAPTIVE_MEMORY_ANCHORS=${ADAPTIVE_MEMORY_ANCHORS}"
echo "[INFO] Using ADAPTIVE_MEMORY_SEARCH_CHUNKS=${ADAPTIVE_MEMORY_SEARCH_CHUNKS}"
echo "[INFO] Results: ${OVO_RESULT_DIR}"

COMMON_ARGS=(
    --model_path "openbmb/MiniCPM-V-4.6" \
    --anno_path "${OVO_ANNO_PATH}" \
    --chunked_dir "${OVO_CHUNKED_DIR}" \
    --result_dir "${OVO_RESULT_DIR}" \
    --recent_frames_only "${ADAPTIVE_MAX_WINDOW}" \
    --chunk_duration 1.0 \
    --fps 1.0 \
    --max_qa_tokens 256 \
    --adaptive-mode "${ADAPTIVE_MODE}" \
    --adaptive-min-window "${ADAPTIVE_MIN_WINDOW}" \
    --adaptive-mid-window "${ADAPTIVE_MID_WINDOW}" \
    --adaptive-max-window "${ADAPTIVE_MAX_WINDOW}" \
    --adaptive-dedup-threshold "${ADAPTIVE_DEDUP_THRESHOLD}" \
    --adaptive-dedup-min-frames "${ADAPTIVE_DEDUP_MIN_FRAMES}" \
    --adaptive-memory-anchors "${ADAPTIVE_MEMORY_ANCHORS}" \
    --adaptive-memory-search-chunks "${ADAPTIVE_MEMORY_SEARCH_CHUNKS}"
)
if [[ -n "${MAX_SAMPLES_PER_SPLIT}" ]]; then
    COMMON_ARGS+=(--max_samples_per_split "${MAX_SAMPLES_PER_SPLIT}")
fi

CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES}" \
ATTN_IMPLEMENTATION="${ATTN_IMPLEMENTATION}" \
MINICPM_DOWNSAMPLE_MODE="${MINICPM_DOWNSAMPLE_MODE}" \
MINICPM_MAX_SLICE_NUMS="${MINICPM_MAX_SLICE_NUMS}" \
MINICPM_PROFILE_COMPONENTS="${MINICPM_PROFILE_COMPONENTS}" \
MINICPM_SERIALIZE_MODEL_LOAD="${MINICPM_SERIALIZE_MODEL_LOAD}" \
MINICPM_MODEL_LOAD_TIMEOUT="${MINICPM_MODEL_LOAD_TIMEOUT}" \
MINICPM_ADAPTIVE_MODE="${ADAPTIVE_MODE}" \
MINICPM_ADAPTIVE_MIN_WINDOW="${ADAPTIVE_MIN_WINDOW}" \
MINICPM_ADAPTIVE_MID_WINDOW="${ADAPTIVE_MID_WINDOW}" \
MINICPM_ADAPTIVE_MAX_WINDOW="${ADAPTIVE_MAX_WINDOW}" \
MINICPM_ADAPTIVE_DEDUP_THRESHOLD="${ADAPTIVE_DEDUP_THRESHOLD}" \
MINICPM_ADAPTIVE_DEDUP_MIN_FRAMES="${ADAPTIVE_DEDUP_MIN_FRAMES}" \
MINICPM_ADAPTIVE_MEMORY_ANCHORS="${ADAPTIVE_MEMORY_ANCHORS}" \
MINICPM_ADAPTIVE_MEMORY_SEARCH_CHUNKS="${ADAPTIVE_MEMORY_SEARCH_CHUNKS}" \
"${PYTHON_BIN}" -m accelerate.commands.launch \
    --num_processes "${NUM_PROCESSES}" \
    --main_process_port "${MAIN_PROCESS_PORT}" \
    --multi_gpu \
    --mixed_precision bf16 \
    main_experiments/minicpm_v46/ovo/eval_adaptive.py "${COMMON_ARGS[@]}"
